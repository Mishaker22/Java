    public void SumarTotal()
    {
        int contar = jTableCarrito.getRowCount();
        float suma=0;
        float total=0;
        int aux=Integer.parseInt(SpinnerCant.getValue().toString());
        for (int i = 0; i < contar; i++) {
            suma=suma+Float.valueOf(jTableCarrito.getValueAt(i,3).toString());  
        }
        txtTotal.setText(String.valueOf(suma));
        
    }
   
    public void CargarDatosTabla()
    {
        LineaProducto.CargarProductos();
        DefaultTableModel modeloDefault=new DefaultTableModel(new String[]{"Id",
            "Denominacion","Proveedor","Stock","Precio"},LineaProducto.listaProductos.size());
        jTablaProductos.setModel(modeloDefault);
        TableModel modeloDatos=jTablaProductos.getModel();
        for (int i=0;i<LineaProducto.listaProductos.size();i++) {
            LineaProducto p=(LineaProducto) LineaProducto.listaProductos.get(i);
            modeloDatos.setValueAt(p.getId(), i, 0);
            modeloDatos.setValueAt(p.getDenominacion(), i, 1);
            modeloDatos.setValueAt(p.getProveedor(), i, 2);
            modeloDatos.setValueAt(p.getCantidad(), i, 3);
            modeloDatos.setValueAt(p.getPrecio(), i, 4);
            
        }
    }
