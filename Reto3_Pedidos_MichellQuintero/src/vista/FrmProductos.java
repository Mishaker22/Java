    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        int aux,value,resta;
        float total=0;
        float auxSuma=0;
        filaSeleccionada=jTablaProductos.getSelectedRow();
        aux=Integer.parseInt(SpinnerCant.getValue().toString());
        
        value=Integer.parseInt(jTablaProductos.getValueAt(filaSeleccionada, 3).toString());
        resta=value-aux;
     
        if(filaSeleccionada!=-1 &aux>0)
        {                  
            if(value>0 && aux<=value)
            {
                jTablaProductos.setValueAt(resta, filaSeleccionada, 3);
                String Datos[]=new String[4];
                Datos[0]=jTablaProductos.getValueAt(filaSeleccionada, 0).toString();
                Datos[1]=jTablaProductos.getValueAt(filaSeleccionada, 1).toString();
                Datos[2]=SpinnerCant.getValue().toString();               
                auxSuma=Float.valueOf(jTablaProductos.getValueAt(filaSeleccionada, 4).toString());
                total=auxSuma*aux;
                Datos[3]=Float.toString(total);
                modelo2.addRow(Datos);
                SumarTotal();               
                
            }else
            {
                JOptionPane.showMessageDialog(null,"Superaste el limite Stock");
            }
        }else{
            JOptionPane.showMessageDialog(null, "ERROR. Debe seleccionar una fila o debe agregar una cantidad");
        }
        SpinnerCant.setValue(0);
        
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

