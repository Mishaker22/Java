    private void btnComprasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprasActionPerformed
        int id, telefono, nit,documentoId;
        String correo, direccion, razonS,nombreUno,nombreDos,apellidoUno,apellidoDos;
        
        id=Integer.parseInt(this.txtIdCliente.getText());
        idCliente=id;
        correo=this.txtCorreoN.getText();
        direccion=this.txtDireccionN.getText();
        telefono=Integer.parseInt(this.txtTelefonoN.getText());
        if(this.jPanelJuridica.isVisible()==true)
        {
            razonS=this.txtRazonSocial.getText();
            nit=Integer.parseInt(this.txtNit.getText());
            Juridica personaJ= new Juridica(razonS,nit,id,direccion,telefono,correo);
            listaClientes.add(personaJ);
            
        }else if(this.jPanelNatural.isVisible()==true){
            nombreUno=this.txtNombre1.getText();
            nombreDos=this.txtNombre2.getText();
            apellidoUno=this.txtApellido1.getText();
            apellidoDos=this.txtApellido2.getText();
            documentoId=Integer.parseInt(this.txtDocumento.getText());
            Natural personaN= new Natural(documentoId,nombreUno,nombreDos,apellidoUno,apellidoDos,id,direccion,telefono,correo);
            listaClientes.add(personaN);
            
        }
        JOptionPane.showMessageDialog(null, "Registro Exitoso");
        LimpiarTexto();
        FrmProductos abrirForm= new FrmProductos();
        abrirForm.setVisible(true);
        
        
        
        //this.txtIdClienteN.setText(String.valueOf(persona.getCorreo()));           
    }//GEN-LAST:event_btnComprasActionPerformed

